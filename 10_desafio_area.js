// Desafio - Calcule a área da Circunferência
// Fórmula - PI * raio * raio

const PI = 3.14;
var raio = 10;
var areaCircu = PI * raio * raio;

console.log("A área da circunferência é de: " + areaCircu + "m²");

areaCircuferencia = Math.PI * raio * raio;
console.log("A area é de: " + areaCircuferencia + "m².");